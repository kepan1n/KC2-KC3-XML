def _generic_hint(path: str) -> str:
    if "/@" in path:
        attr = path.split("/@")[-1]
        return f"Атрибут «{attr}». Заполняется по правилам формата ФНС и связанным условиям раздела."
    node = path.split("/")[-1]
    return f"Элемент «{node}». Заполняется по структуре XSD и логике выбранного режима документа."


def build_embedded_hints(field_paths: Iterable[str], xsd_docs: dict[str, str] | None = None) -> dict[str, str]:
    out: dict[str, str] = {}
    xsd_docs = xsd_docs or {}
    for p in field_paths:
        if p in EXACT_HELP:
            base = EXACT_HELP[p]
        else:
            base = ""
            for pref, txt in PREFIX_HELP:
                if p.startswith(pref):
                    base = txt
                    break
            if not base:
                base = _generic_hint(p)

        xdoc = (xsd_docs.get(p) or "").strip()
        if xdoc:
            out[p] = f"{base}\n\nОписание из XSD: {xdoc}"
        else:
            out[p] = base
    return out
